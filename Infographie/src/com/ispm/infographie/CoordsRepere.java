package infographie;

public class CoordsRepere { //Coordonnées d'un point dans le repère
    private float xR; // 
    private float yR; // 
    public CoordsRepere(float xR, float yR) {
        this.xR = xR;
        this.yR = yR;
    }   
    public float getxR() {
        return xR;
    }
    public float getyR() {
        return yR;
    }

}